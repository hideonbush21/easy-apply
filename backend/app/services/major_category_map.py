def get_category(major: str) -> str | None:
    """
    返回专业大类。未匹配到返回 None。
    优先精确匹配，其次关键词匹配。
    """
    if not major:
        return None
    major = major.strip()

    # 过滤无效值
    if major in ("暂无数据", "暂无参考资料", "暂无参考材料", "无", "N/A", "-", "—"):
        return None

    # 精确匹配
    if major in EXACT_MAP:
        return EXACT_MAP[major]

    # 关键词匹配
    for keywords, category in KEYWORD_RULES:
        for kw in keywords:
            if kw in major:
                return category

    return None
